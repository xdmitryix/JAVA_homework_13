public class Calculator {
    
    public static Integer sum(int first, int second) {
        int res = first + second;
        return res;
    }


    public static Integer multi(int first, int second){
        int res = first * second;
        return res;
    }

    public static Integer minus(int first, int second) {
        int res = first - second;
        return res;
    }
}
